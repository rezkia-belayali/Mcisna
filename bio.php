    <h1>Carte Nationale d’Identité</h1>
    <p>
        <strong> dossier de demande de la carte nationale d’identité comprend :</strong><br>
        Le formulaire renseigné et signé par l’intéressé ou par le tuteur légal pour les mineurs, auquel il est joint :<br>
        1. l’extrait d’acte de naissance n°12 de l’intéressé;<br>
        2. le certificat de nationalité algérienne, lorsque la demande est exprimée pour la première fois ;<br>
        3. l’acte de naissance du père ou de la mère, ou à défaut, l’acte de décès de l’un des deux parents;<br>
        4. le certificat de résidence datant de moins de six (6) mois ;<br>
        5. la fiche familiale d’état civil pour les mariés ;<br>
        6. quatre (4) photos d’identité en couleur, numériques, récentes et identiques ;<br>
        7. une quittance fiscale ou timbre fiscal dont le montant correspond à la nature du document demandé ;<br>
        8. la copie de la carte du groupe sanguin.<br>
        Il n'y a pas de condition d’âge pour la délivrance de la carte nationale d’identité.<br>
        La durée de validité de la carte nationale d’identité est de dix (10) ans à compter de la date de signature.<br>
        
      <a href="https://www.interieur.gov.dz/index.php/fr/circulation-de-personnes/la-carte-nationale-d-identite.php#faqnoanchor" class="get-started-btn">pour la demande en ligne</a>
    </p>
    <h1>Passeport biométrique</h1>
    <p>Le dépôt du dossier de demande du passeport biométrique électronique s’effectue auprès de n’importe quelle commune, circonscription administrative ou daïra de la même wilaya ou auprès du service consulaire du lieu de résidence pour les algériens résidents à l’étranger.
        Le dépôt confirmé du dossier et l’enrôlement des données biométriques donnent lieu à la délivrance d’un récépissé par le responsable habilité
      <a href="https://www.interieur.gov.dz/index.php/fr/mes-d%C3%A9marches-administratives/passeport-biom%C3%A9trique.php#faqnoanchor" class="get-started-btn">voir plus</a>
    </p>
    <h1>Permis de conduire</h1>
    <p>

        Le dépôt de dossier de délivrance du permis de conduite se fait au niveau de la daïra, ou de la circonscription administrative, lieu de résidence de l’intéressé.
        
      <a href="https://www.interieur.gov.dz/index.php/fr/mes-d%C3%A9marches-administratives/permis-de-conduire.php#faqnoanchor" class="get-started-btn">voir plus</a>
