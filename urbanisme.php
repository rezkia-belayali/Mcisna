      <h1>Permis De Lotir</h1>
      <p>
        <strong> dossier a fournir: </strong><br>
        
        La demande de permis de lotir dont le modèle-type est joint en annexe du présent décret, doit être formulée et signée par le propriétaire ou son mandataire. L'intéressé doit fournir à l'appui de sa demande :<br>

    *soit une copie de l'acte de propriété <br>
    *soit un mandat conformément aux dispositions de l'ordonnance n°75-58 du 26 septembre 1975, susvisée .<br>
    *soit une copie du statut lorsque le propriétaire ou le mandataire est une personne morale.<br>
    le plan de situation établi à une échelle appropriée permettant la localisation du terrain .<br>
    les plans utiles à l'échelle 1/200ème ou 1/500ème comportant les indications suivantes :<br>
        -les limites de terrain et sa superficie.<br>
        -les courbes de niveau et la surface de nivellement,avec leurs caractéristiques techniques principales et les points de raccordement des voies et réseaux divers .<br>
        -la délimitation des lots projetés avec le tracé de la voirie, des réseaux d'alimentation en eau potable, d'incendie, d'évacuation des eaux usées ainsi que des réseaux de distribution de gaz, d'électricité, detéléphone et d'éclairage public.<br>
        -la localisation des aires de stationnement et des espaces libres et celle des servitudes particulières .<br>
        -l'implantation, la nature et la forme urbaine des constructions projetées y compris celles relatives aux équipements collectifs .<br>
    une notice précisant les dispositifs relatifs :<br>
        -au traitement destiné à débarrasser les eaux résiduaires industrielles de toute substance liquide, solide ou gazeuse préjudiciable à la santé publique, à l'agriculture ou à l'environnement .<br>
        -au traitement destiné à débarrasser les fumées et émissions gazeuses de toute substance préjudiciable à la santé publique.<br>
        -à la limitation du niveau de bruit et d'émission de parasites électromagnétiques, pour les lotissements à usage industriel .<br>

    une notice comportant les indications suivantes :<br>
        -la liste des lots et leur superficie respective .<br>
        -la nature des diverses occupations précisant le nombre de logements, d'emplois et la population totale résidente .<br>
        -les besoins induits en eau, gaz, électricité, transport et les modalités de leur satisfaction .<br>
        -la nature des servitudes et nuisances éventuelles.<br>
        -le cas échéant, l'étude d'impact sur l'environnement .<br>
        -une étude géotechnique élaborée par un laboratoire agréé.<br>

    le programme des travaux indiquant les caractéristiques techniques des ouvrages, réseaux et aménagements à réaliser et les conditions de leur mise en œuvre, avec une estimation de leur coût et précisant, le cas échéant, les tranches de réalisation et leurs délais .
    un cahier des charges fixant les obligations et servitudes fonctionnelles et d'intérêt général, imposées dans le lotissement ainsi que les conditions dans lesquelles les constructions doivent être édifiées.<br>

