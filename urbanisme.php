      <p>
        <strong> dossier a fournir: </strong><br>
        
        La demande de certificat d’urbanisme peut être établie par le propriétaire ou son mandataire ou toute personne intéressée et doit comporter les indications suivantes :

       une demande manuscrite signée par le demandeur.<br>
      le nom du propriétaire du terrain.<br>
      un plan de situation permettant la localisation du terrain.<br>
      un plan précisant les limites du terrain pour ceux situés dans la limite du périmètre urbain.<br>

      La demande de certificat d’urbanisme ou la fiche de renseignements et les pièces qui l'accompagnent sont déposées en deux (2) exemplaires au siège de l'assemblée populaire communale territorialement compétente, contre récépissé de dépôt remis le jour même.<br>
