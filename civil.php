    <h1>Acte de Naissance</h1>
    <p>La naissance de l’enfant est déclarée par le père ou la mère ou par les docteurs en médecine, les sages femmes ou autres personnes qui ont assisté à l’accouchement.
      <h4>La constitution de dossier</h4> 
      Le déclarant doit présenter  le livret de famille, et le cas échéant, les actes de naissance du père et de la mère et l’acte de leur mariage.
      <a href="https://www.interieur.gov.dz/index.php/fr/mes-d%C3%A9marches-administratives/etat-civil/acte-de-naissance.php" class="get-started-btn">pour la demande en ligne</a>
    </p>
    <h1>Extrait de naissance 12-S</h1>
    <p>L’Acte de Naissance Spécial 12S est un document officiel contenant des informations d’état civil permettant la génération et l’attribution d’un Numéro d’identifiant National unique à chaque citoyen.
      Le 12S est un document authentique :<br>
      - Il est délivré par la commune de naissance sur la base du registre d’Etat Civil des naissances.<br>
      - Il est établi par le biais d’une plateforme informatique sécurisée et    suivant des procédures strictes de validation hiérarchique.<br>
      Le 12S est un document sécurisé :<br>
      - Il est édité sur un support papier fiduciaire comportant le sceau de    l’état en filigrane, des impressions de sécurité et une bande    métallique à couleurs variables intégrée dans le papier.<br>
      - Il comporte un numéro de série unique établissant la traçabilité du   document et permettant de détecter toute reproduction.
      <a href="https://demande12s.interieur.gov.dz/Fr/WFInscription.aspx" class="get-started-btn">pour la demande en ligne</a>  
    </p>
    <h1>Acte de mariage</h1>
    <p>L’un et l’autre, des futurs époux doivent justifier de leur état civil par la production de l’un des documents suivants ;<br>
      1 -Extrait datant de moins de trois (3) mois, soit de l’acte de naissance, soit de la transcription du jugement individuel ou collectif de naissance ;<br>
      2 - fiche de résidence de l’un des époux ;<br>
      3 - Certificat médical des deux époux ;<br>
      4 -  Et selon le cas :<br>
      * L’autorisation du mariage pour les militaires et corps de sécurité ;<br>
      * La dispense de la condition d’âge pour les mineurs ;<br>
      * Le livret de famille relatif à un précédent mariage.<br>
      * La femme dont le précédent mariage a été dissous, doit produire:<br>
      -  Soit l’acte de décès du précédent mari ou l’acte de naissance de celui-ci portant mention du décès , ou bien le livret de famille ou figure l’acte de décès ;<br>
      -  Soit un extrait de l’acte de mariage ou de naissance portant mention de la dissolution du mariage ou le livret de famille ou figure cette mention ou bien encore, la décision de dissolution accompagnée d’une attestation du magistrat ou du greffier compétent certifiant qu’elle est devenue définitive ;<br>
      5 - Les deux témoins doivent présenter leurs carte nationale d’identité ou n’importe quel document prouvant les identifiés.
      <a href="https://www.interieur.gov.dz/index.php/fr/mes-d%C3%A9marches-administratives/etat-civil/acte-de-mariage.php" class="get-started-btn">pour la demande en ligne</a> 
      </p>
    <h1>Fiche familiale d'état civil</h1> 
    <p>L’administration concernée<br>

      Pour l'obtention de la fiche familiale d'état civil, le demandeur doit se déplacer aux services de l'état civil du lieu de sa résidence.<br>
      
      Les documents demandés<br>
      
      La fiche familiale d'état civil se délivre au vu du livret de famille, sans demander d'autres documents, cette condition est justifiable que la fiche familiale d'état civil constitue en même temps :<br>
      
      ·         un extrait de naissance des époux et leurs enfants<br>
      
      ·         certificat de vie<br>
      
      ·         acte de mariage et certificat de divorce des époux et, le cas échéant,  des actes de décès.<br>
      
       
      Le délai de  la délivrance de la fiche familiale<br>
      
      Comme une règle générale, la délivrance de la fiche familiale d'état civil se fait immédiatement.
      <a href="#" class="get-started-btn">pour la demande en ligne</a> 
      
    </p> 
    <h1>Certificat de résidence et fiche de résidence</h1>
    <p>

      <strong> constitutifs du dossier de la demande de Certificats et fiche de résidence</strong>
      
      Le certificat de résidence est délivré dans trois (03) cas :<br>
      
          Demande de la CNI,<br>
          Demande de passeport,<br>
          Création d’un parti politique.<br>
      
      Pour le reste des cas le certificat est remplacé par la fiche de résidence.<br>
      
      <strong>Présenter un document justificatif:</strong><br>
      
          Copie du titre de propriétaire en vigueur,<br>
          Contrat de location ou la dernière quittance de loyer,<br>
          Copie d’affectation pour le logement de fonction ou de service,<br>
          Dernière quittance des services de la SONELGAZ,<br>
          Dernière quittance des services des EAUX et de TELEPHONE,<br>
          Carte d’électeur,<br>
          Certificat d’hebergement,<br>
          Autre document officiel justifiant l’occupation de logement.
          <a href="https://www.interieur.gov.dz/index.php/fr/mes-d%C3%A9marches-administratives/etat-civil/le-certificat-et-fiche-de-r%C3%A9sidence.php#faqnoanchor" class="get-started-btn">pour la demande en ligne</a> 

    </p>
  <h1>Livret de Famille</h1>
  <p>
    <strong>Procédure de délivrance et de  renouvellement du livret de famille</strong><br>

      Lorsque le mariage a lieu devant l’officier de l’état civil, celui-ci en dresse acte sur le champ dans les registres ; il remet aux époux un livret de famille constatant le mariage,
  
      Lorsque le mariage est célébré en pays étranger par l’autorité locale compétente, le livret de famille est établit et remis aux époux par l’agent diplomatique ou consulaire territorialement compétent lors de la célébration de l’acte de mariage.
  
      En cas de divorce, un second livret peut être remis sur sa demande, à celui des époux qui est dépourvu du premier livret,<br>
  
      En cas de perte, de vol ou de destruction de livret de famille, celui-ci est reconstitué par un deuxième livret à la requête du chef de famille,<br>
  
     dans ce cas :<br>
  
      - L’extrait d’acte de mariage et éventuellement les autres actes, établis dans la même commune, sont portés immédiatement sur le nouveau livret de famille,<br>
      - Il est établi ou transcrit l’acte ou les actes dont les extraits figurent ensuite sur le premier livret par ordre « Naissance, Mariage, Décès, Jugement et Ordonnances judiciaire » sur le nouveau livret par les services d’état civil des communes dont ces actes sont inscrits,<br>
      - Le second livret de famille porte la mention selon le cas : « second livret délivré après divorce »,  « second livret délivré après disparition du premier ».<br>
      <a href="https://www.interieur.gov.dz/index.php/fr/mes-d%C3%A9marches-administratives/etat-civil/le-livret-de-famille.php" class="get-started-btn">pour la demande en ligne</a>     
  </p>
  <h1>Acte de décès</h1>
  <p><strong>Les personnes habilitées à la déclaration</strong><br>

    La déclaration de décès se fait par l'un des personnes suivantes :<br>
    
    ·         Parent de défunt<br>
    
    ·         Personne possédant sur son état civil les renseignements les plus exacts et les plus complets possibles.<br>
    
    ·         Les hôpitaux ou les structures de santé ou les hôpitaux maritimes.<br>
    
    ·         Les responsables des établissements pénitentiaires, …Etc.<br>
    
    

    Les documents demandés<br>
    
    L'officier de l'état civil ne peut délivrer l'acte de décès que sur production d'un certificat établi par un médecin ou, à défaut, par l'officier de police judiciaire qu'il a chargé de s'assurer du décès.<br>
    
    La présentation du livret famille et, le cas échéant, un extrait de naissance du défunt.<br>
    
    La délivrance d'un extrait de décès<br>
    
    Le demandeur de l'extrait de décès a le droit de choisir entre sa présence lui-même au service de l'état civil de la commune, lieu de décès ou l'envoi d'une demande manuscrite par voie postale :<br>
    
    ·         Le premier cas : Il doit lui identifier en précisant le degré de parenté avec la personne décédée, et fournir tous les renseignements qui faciliteront la recherche.<br>
    
    ·         Le deuxième cas : Il doit envoyer une demande dans une enveloppe timbrée et libellée à l'adresse du demandeur ;  comporte le nom, le prénom, la date et lieu du décès du défunt et la filiation.<br>
    <a href="https://www.interieur.gov.dz/index.php/fr/mes-d%C3%A9marches-administratives/etat-civil/acte-de-d%C3%A9c%C3%A8s.php" class="get-started-btn">pour la demande en ligne</a> 
  </p>
